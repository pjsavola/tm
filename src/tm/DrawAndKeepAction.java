package tm;

import java.util.ArrayList;
import java.util.List;

public class DrawAndKeepAction extends SelectCardsAction {

    public DrawAndKeepAction(int amount, final Game game) {
        super(drawCards(amount, game), game);
    }

    @Override
    public char getKey() {
        return 'd';
    }

    private static List<Card> drawCards(final int amount, final Game game) {
        int cardsToDraw = amount;
        final List<Card> selection = new ArrayList <>();
        while (cardsToDraw-- > 0) {
            selection.add(game.drawCard());
        }
        return selection;
    }
}
